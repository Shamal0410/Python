'''
Arithmetic operators

+ addition
- subtraction
* multiplication
/ division
% modulus
** exponent
// floor division
'''

a=35
b=4
print("Addititon: ",(a+b))
print("Subtraction: ",(a-b))
print("Multiplication: ",(a*b))
print("Division: ",(a/b))
print("Modulus of remainder: ",(a%b))
print("Exponent: ",(a**b))
print("Floor Division: ",(a//b))
